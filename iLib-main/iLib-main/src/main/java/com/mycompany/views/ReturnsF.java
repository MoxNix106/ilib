    private void buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonActionPerformed
        String selectedUser = (String) ComboUsuarios.getSelectedItem();
        String selectedBook = (String) ComboLibros.getSelectedItem();

        if (selectedUser != null && selectedBook != null 
                && !selectedUser.startsWith("No hay") && !selectedBook.startsWith("No hay")) {
            try {
                DAOUsers daoUsers = new DAOUsersImpl();
                DAOBooks daoBooks = new DAOBooksImpl();
                DAOLendings daoLendings = new DAOLendingsImpl();

                int userId = daoUsers.getUserIdByName(selectedUser);
                int bookId = daoBooks.getBookIdByTitle(selectedBook);

                Lendings lending = daoLendings.getLending(new Users(userId), new Books(bookId));

                if (lending == null) {
                    JOptionPane.showMessageDialog(this, "No existe un préstamo para este usuario y libro.");
                    return;
                }

                lending.setDate_return(java.time.LocalDate.now().toString());
                daoLendings.modificar(lending);
                JOptionPane.showMessageDialog(this, "Libro devuelto con éxito.");

            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Error al registrar la devolución: " + e.getMessage());
            }
        } else {
            JOptionPane.showMessageDialog(this, "Por favor seleccione un usuario y un libro.");
        }


    }//GEN-LAST:event_buttonActionPerformed

    private void botonBuscarLibrosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonBuscarLibrosActionPerformed

        String search = BuscarLibros.getText().trim();
        if (search.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Ingrese un título de libro para buscar.");
            return;
        }
        try {
            DAOBooks daoBooks = new DAOBooksImpl();
            List<Books> listaLibros = daoBooks.listar(search);
            ComboLibros.removeAllItems();
            if (listaLibros.isEmpty()) {
                JOptionPane.showMessageDialog(this, "No se encontraron libros con ese título.");
            } else {
                for (Books b : listaLibros) {
                    ComboLibros.addItem(b.getTitle());
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al buscar libros: " + e.getMessage());
        }
    }//GEN-LAST:event_botonBuscarLibrosActionPerformed

