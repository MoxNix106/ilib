    private void loadInitialData() {
        try {
            // Cargar todos los usuarios
            DAOUsers daoUsers = new DAOUsersImpl();
            List<Users> listaUsuarios = daoUsers.listar("");
            ComboUsuarios.removeAllItems();
            if (listaUsuarios.isEmpty()) {
                ComboUsuarios.addItem("No hay usuarios registrados");
            } else {
                for (Users u : listaUsuarios) {
                    ComboUsuarios.addItem(u.getName());
                }
            }

            // Cargar todos los libros
            DAOBooks daoBooks = new DAOBooksImpl();
            List<Books> listaLibros = daoBooks.listar("");
            ComboLibros.removeAllItems();
            if (listaLibros.isEmpty()) {
                ComboLibros.addItem("No hay libros registrados");
            } else {
                for (Books b : listaLibros) {
                    ComboLibros.addItem(b.getTitle());
                }
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al cargar datos iniciales: " + e.getMessage());
        }
    }

