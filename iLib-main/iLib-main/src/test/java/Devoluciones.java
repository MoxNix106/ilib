    private void buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonActionPerformed
   String selectedUser = (String) ComboUsuarios.getSelectedItem();
    String selectedBook = (String) ComboLibros.getSelectedItem();

    if (selectedUser != null && selectedBook != null) {
        try {
            DAOUsers daoUsers = new DAOUsersImpl();
            DAOBooks daoBooks = new DAOBooksImpl();
            DAOLendings daoLendings = new DAOLendingsImpl();

            // Obtener los IDs del usuario y del libro
            int userId = daoUsers.getUserIdByName(selectedUser);
            int bookId = daoBooks.getBookIdByTitle(selectedBook);

            // Obtener el préstamo activo del usuario y libro seleccionados
            Lendings lending = daoLendings.getLending(new Users(userId), new Books(bookId));

            if (lending == null) {
                javax.swing.JOptionPane.showMessageDialog(this, "No existe un préstamo activo para este usuario y libro.");
                return;
            }

            // Registrar la devolución
            lending.setDate_return(java.time.LocalDate.now().toString());
            daoLendings.modificar(lending);

            // Confirmación
            javax.swing.JOptionPane.showMessageDialog(this, "Libro devuelto con éxito.");
        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Error al registrar la devolución: " + e.getMessage());
        }
    } else {
        javax.swing.JOptionPane.showMessageDialog(this, "Por favor seleccione un usuario y un libro.");
    }


    }//GEN-LAST:event_buttonActionPerformed

    private void botonBuscarLibrosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonBuscarLibrosActionPerformed
       
    String search = bucarUsuarios.getText().trim();
    if (search.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Ingrese un nombre de usuario para buscar.");
        return;
    }
    try {
        DAOUsers daoUsers = new DAOUsersImpl();
        List<Users> listaUsuarios = daoUsers.listar(search);
        ComboUsuarios.removeAllItems();
        if (listaUsuarios.isEmpty()) {
            JOptionPane.showMessageDialog(this, "No se encontraron usuarios con ese nombre.");
        } else {
            for (Users u : listaUsuarios) {
                ComboUsuarios.addItem(u.getName());
            }
        }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error al buscar usuarios: " + e.getMessage());
    }



   

    }//GEN-LAST:event_botonBuscarLibrosActionPerformed

