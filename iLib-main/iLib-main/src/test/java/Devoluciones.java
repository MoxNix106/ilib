       /**
     * Este método realiza una búsqueda de usuarios por nombre (o parte del nombre)
     * y actualiza el ComboUsuarios con los resultados.
     * Suponiendo que en DAOUsers hay un método getUsersByName(String) que retorna una lista.
     */


private void cargarUsuarios() {
    try {
        DAOUsers daoUsers = new DAOUsersImpl();
        List<Users> listaUsuarios = daoUsers.listar("");

        ComboUsuarios.removeAllItems();
        for (Users u : listaUsuarios) {
            ComboUsuarios.addItem(u.getName());
        }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error al cargar usuarios: " + e.getMessage());
    }
}

private void cargarLibros() {
    try {
        DAOBooks daoBooks = new DAOBooksImpl();
        List<Books> listaLibros = daoBooks.listar("");
        ComboLibros.removeAllItems();
        for (Books b : listaLibros) {
            ComboLibros.addItem(b.getTitle());
        }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error al cargar libros: " + e.getMessage());
    }
}

